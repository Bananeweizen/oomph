  public void storeCatalogs() throws IOException
  {
    ResourceSet resourceSet = createResourceSet();

    Index index = SetupFactory.eINSTANCE.createIndex();
    Resource indexResource = resourceSet.createResource(URI.createFileURI(new File(DATA, "org.eclipse.setup").toString()));
    indexResource.getContents().add(index);

    EList<ProjectCatalog> projectCatalogs = createProjectCatalogs();
    index.getProjectCatalogs().addAll(projectCatalogs);

    EList<ProductCatalog> productCatalogs = index.getProductCatalogs();
    for (ProjectCatalog projectCatalog : projectCatalogs)
    {
      Resource catalogResource = resourceSet.createResource(URI.createFileURI(new File(PROJECT_CATALOGS, ids.get(projectCatalog).substring(
          CLASSIFICATION.length())
          + ".setup").toString()));
      catalogResource.getContents().add(projectCatalog);

      for (Project project : projectCatalog.getProjects())
      {
        Resource projectResource = resourceSet.createResource(URI.createFileURI(new File(PROJECTS, ids.get(project).substring(PRODUCT.length()) + ".setup")
            .toString()));
        projectResource.getContents().add(project);
      }

      productCatalogs.add(createProductCatalog(resourceSet, projectCatalog));
    }

    for (Resource resource : resourceSet.getResources())
    {
      resource.save(null);
    }
  }

  public static EList<ProjectCatalog> loadCatalogs(boolean local)
  {
    ResourceSet resourceSet = createResourceSet();

    EList<ProjectCatalog> catalogs = new BasicEList<ProjectCatalog>();
    for (File file : PROJECT_CATALOGS.listFiles())
    {
      URI uri = local ? URI.createFileURI(file.toString()) : URI.createURI("http://download.eclipse.org/modeling/emf/cdo/data/project_catalogs/"
          + file.getName());
      Resource resource = resourceSet.getResource(uri, true);
      catalogs.add((ProjectCatalog)resource.getContents().get(0));
    }

    return catalogs;
  }

  public Index createIndex()
  {
    EList<ProjectCatalog> catalogs = createProjectCatalogs();
    return createIndex(catalogs);
  }

  public static Index createIndex(EList<ProjectCatalog> catalogs)
  {
    Index index = SetupFactory.eINSTANCE.createIndex();
    index.getProjectCatalogs().addAll(catalogs);
    return index;
  }

  private ProductCatalog createProductCatalog(ResourceSet resourceSet, ProjectCatalog projectCatalog)
  {
    ProductCatalog productCatalog = SetupFactory.eINSTANCE.createProductCatalog();
    productCatalog.setName(projectCatalog.getName() + " Products");

    InstallationTask installationTask = SetupFactory.eINSTANCE.createInstallationTask();
    installationTask.setID("installation");
    productCatalog.getSetupTasks().add(installationTask);

    Resource catalogResource = resourceSet.createResource(URI.createFileURI(new File(PRODUCT_CATALOGS, ids.get(projectCatalog).substring(
        CLASSIFICATION.length())
        + ".setup").toString()));
    catalogResource.getContents().add(productCatalog);

    for (Project project : projectCatalog.getProjects())
    {
      int i = 0;
      for (Project subProject : project.getProjects())
      {
        Product product = SetupFactory.eINSTANCE.createProduct();
        product.setName(project.getName() + " - " + subProject.getName() + " Product");
        productCatalog.getProducts().add(product);

        Resource productResource = resourceSet.createResource(URI.createFileURI(new File(PRODUCTS, ids.get(project).substring(PRODUCT.length()) + "-" + ++i
            + ".setup").toString()));
        productResource.getContents().add(product);

        VariableTask variable = SetupFactory.eINSTANCE.createVariableTask();
        variable.setName("installation.location");
        product.getSetupTasks().add(variable);

        EList<ProductVersion> versions = product.getVersions();
        for (Stream stream : subProject.getStreams())
        {
          ProductVersion productVersion = SetupFactory.eINSTANCE.createProductVersion();
          productVersion.setName(stream.getName());
          versions.add(productVersion);

          VariableTask bogusVariable = SetupFactory.eINSTANCE.createVariableTask();
          bogusVariable.setName("bogus");
          productVersion.getSetupTasks().add(bogusVariable);

          // P2Task p2Task = SetupP2Factory.eINSTANCE.createP2Task();
          // productVersion.getSetupTasks().add(p2Task);
          //
          // Requirement requirement = P2Factory.eINSTANCE.createRequirement();
          // requirement.setVersionRange(new VersionRange("1.0.0"));
          // p2Task.getRequirements().add(requirement);
          //
          // Repository repository = P2Factory.eINSTANCE.createRepository();
          // repository.setURL("http://www.eclipse.org/bogus");
          // p2Task.getRepositories().add(repository);
        }
      }
    }

    return productCatalog;
  }

